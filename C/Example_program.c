/* កម្មវិធីសរសេរកូដ C ទូទៅមានរចនាសម្ព័ន្ធដូចខាងក្រោម៖*/

// Preprocessor Directives (ការណែនាំមុនពេលចងក្រង): ដូចជាអ្នកត្រៀមសម្ភារៈមុនពេលចម្អិនអាហារ។
// Main Function (មុខងារចម្បង): ដូចជាផ្ទះបាយក្នុងភោជនីយដ្ឋាន។
// Variable Declarations (សេចក្តីប្រកាសអថេរ): ដូចជាការរៀបចំឧបករណ៍ចម្អិនអាហារ។
// Statements and Expressions (សេចក្តីថ្លែងការណ៍ និងការបញ្ចេញមតិ): ដូចជាជំហាននៃការធ្វើម្ហូប។
// Functions (មុខងារ): ដូចជាបច្ចេកទេសចម្អិនផ្សេងៗ។

#include <stdio.h> // Preprocessor directive

int main() { // Main function
    int a = 10; // Variable declaration and initialization
    printf("តម្លៃរបស់ a គឺ: %d\n", a); // Statement to print the value of a
    return 0; // Return statement
}